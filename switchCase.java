public class switchCase {
    public static void main(String[] args) {
        int num = 10;

        switch (num) {
            case 10: {
                System.out.println("Number is = 10");
            }
            case 20: {
                System.out.println("Number is = 20");
            }
            case 30: {
                System.out.println("Number is = 30");
            }
            default: {
                System.out.println("Number is = 00");
            }
        }
    }
}
