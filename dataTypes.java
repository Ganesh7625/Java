public class dataTypes {
    public static void main(String[] args) {
        int Int = 9; // Interger/Numbers
        long Long = 99999999L; // numbers long values
        float Float = 2.5f; // Decimal values
        double Double = 3.78; // Decimal long Values
        char Character = 'a'; // Charaters single char
        String str = "Something"; // strings

        System.out.println(Int);
        System.out.println(Long);
        System.out.println(Float);
        System.out.println(Double);
        System.out.println(Character);
        System.out.println(str);

    }
}
